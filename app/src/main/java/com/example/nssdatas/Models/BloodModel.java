package com.example.nssdatas.Models;

public class BloodModel {
    String address;
    String blood_for_whom;
    String email;
    String id_roll;
    String name;
    String phone;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBlood_for_whom() {
        return blood_for_whom;
    }

    public void setBlood_for_whom(String blood_for_whom) {
        this.blood_for_whom = blood_for_whom;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getId_roll() {
        return id_roll;
    }

    public void setId_roll(String id_roll) {
        this.id_roll = id_roll;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
